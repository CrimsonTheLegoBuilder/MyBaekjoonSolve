import sys
sys.stdin = open("input_1865.txt", 'r')


def dfs(x, r, t):
    global n_l
    if t < n_l:
        return
    if not t:
        n_l = max(n_l, t)
        return
    if x == N:
        n_l = max(n_l, t)
        return
    for i in range(N):
        if not r & 1 << i:
            dfs(x + 1, r | 1 << i, t * P[x][i] / 100)


for tc in range(int(input())):
    N = int(input())
    P = [tuple(map(int, input().split())) for _ in range(N)]
    n_l = 0
    dfs(0, 0, 1)
    print(f"#{tc + 1} {n_l*100:.6f}")


